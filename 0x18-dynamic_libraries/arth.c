#include <stdio.h>

/**
 * add - gets the sum of two integers
 * @x: first integer
 * @y: second integer
 * Return: the sum of the two integers
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - gets the difference of two integers
 * @x: first integer
 * @y: second integer
 * Return: the difference of the two integers
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mod - gets the remainder of two integers
 * @x: first integer
 * @y: second integer
 * Return: the remainder of the two integers
 */
int mod(int x, int y)
{
	return (x % y);
}

/**
 * mul - gets the multiplication of two integers
 * @x: first integer
 * @y: second integer
 * Return: the multiplication of the two integers
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - gets the division of two integers
 * @x: first integer
 * @y: second integer
 * Return: the division of the two integers
 */
int div(int x, int y)
{
	return (x / y);
}
